					<h2>Mona Lisa</h2>
					<h3>A little cliche</h3>

					<p>De Mona Lisa is natuurlijk een kunstwerk waar iedereen van gehoord heeft. Een beetje cliche maar absoluut noemenswaardig. Afgezien van de impressive technieken geeft het schilderij een omschrijving van de aard van Leonardo.</p>

					<div class="img-placeholder mona"></div>

					<p class="text-justified">Leonardo was zo obsessief gedetailleerd dat wordt gezegd dat hij er spijt van heeft nooit een werkt afgemaakt te hebben. Dit lijkt ook daadwerkelijk het geval wanneer wordt gekeken naar de Mona Lisa. Volgens onderzoek is Leonardo rond 1503/1504 beginnen met het schilderen van het meesterwerk om na 4 jaar werk het schilderij onvoltooid achter te laten. in 1516 was Leonardo uitgenodigd door Koning Francois en men geloofd dat hij daar het schilderij mee naartoe heeft genomen om tot 1517 het werk te blijven verfijnen. Dit level van detail en de drang om alle werken tot in perfectie uit te voeren is niet alleen te zien in de Mona Lisa maar dit is een van de betere voorbeelden.</p>

					<h4>The smile</h4>

					<p class="text-justified">Het meest onderzochte deel van de Mona Lisa is de glimlach. Leonardo heeft opzettelijk gebruik gemaakt van illusies om het te doen lijken alsof Mona Lisa anders lacht vanuit verschillende ooghoeken. Wanneer je recht op de mond kijkt lijkt het alsof de mond duidelijk naar beneden afbuigt. Wanneer je hier verder van afwijkt en elders kijkt op het schilderij kijkt lijkt de mond echter een opwaartse buiging te maken. Een glimlach die alleen indirect gezien kan worden. Dit heeft te maken met de techniek genaamd sfumato waarbij het spelen met verschillende contrasten en kleuren deze illusie wordt gecreeerd.

					<span class="quote">As the smile disappears as soon as the viewer tries to 'catch it', we have named this visual illusion the 'uncatchable smile'
					<span class="quotee">Alessandro Soranzo
					Michelle Newberry of Sheffield Hallam University </span></span>
	
					</p>

					<div class="img-placeholder perspective"></div>

					<p class="text-justified">Veel verschillende artiesten hebben geprobeerd deze techniek toe te passen maar zijn het er univorm over eens dat niemand de kwaliteit van Leonardo kan evenaren.</p>

					<p class="text-justified">Onderzoekers onderworpen de monalisa en vergelijkbare kunstwerken aan een reeks experimenten om te onderzoeken hoe de verschillende standpunten en niveaus van onscherpte in de beelden zelf de perceptie van de kijker zou kunnen veranderen</p>
